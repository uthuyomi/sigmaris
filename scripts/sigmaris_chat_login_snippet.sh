# Sigmaris CLIチャット: ログインシェルへの組み込み例。
#
# このファイル自体は実行しない(自動適用もしない)——運用者が、内容を
# 確認した上で、サーバー上の ~/.bash_profile (または ~/.profile) の末尾に
# 手動で追記することを想定している(依頼書「マイグレーションが必要な
# 場合は作成のみ行い、適用は運用者側に委ねる」と同じ扱い。本タスクは
# シェル設定ファイルではなく、あくまでコードの実装のみを行う)。
#
# 判断根拠:
#   - `[[ $- == *i* ]]`: 対話型シェルの場合のみ起動する(scpやrsync等、
#     非対話的なSSHコマンド実行時に、誤って起動しないようにするため)。
#   - `[[ -z "${TMUX:-}" ]]`: 既にtmuxセッションの中にいる場合は起動しない
#     (tmuxの新規セッションが、設定によっては新しいログインシェルを
#     生成することがあり、無限にネストして自動起動を繰り返す事態を防ぐ)。

if [[ $- == *i* ]] && [[ -z "${TMUX:-}" ]]; then
    ~/sigmaris_chat_autostart.sh
fi
